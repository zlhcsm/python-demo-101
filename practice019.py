# 题目：一个数如果恰好等于它的因子之和，这个数就称为"完数"。
# 例如6=1＋2＋3.编程找出1000以内的所有完数。

# 程序源代码
# ======================================================

score = int(input("输入分数：\n"))
if score > 90:
    grade = 'A'
elif score >= 60:
    grade = 'B'
else:
    grade = 'C'

print('%d 属于 %s' %(score, grade))

# ======================================================

# 输出结果:
# 89
# 89 属于 B
